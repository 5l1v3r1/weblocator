def split_list(arr, size):
    arrs = []
    while len(arr) > size:
        pice = arr[:size]
        arrs.append(pice)
        arr = arr[size:]
    arrs.append(arr)
    return arrs


def print_message(message):
    sys.stdout.write(message)
    sys.stdout.flush()


def is_online(host):
    try:
        socket.gethostbyname(host)
    except socket.gaierror:
        return False
    return True


